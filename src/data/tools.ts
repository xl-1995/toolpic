export function getToolBySlug(slug: string, locale: string): ToolDef | undefined {
  return tools.find((t) => t.slugs[locale] === slug || t.slugs['en'] === slug);
}

export function getToolSlug(toolId: string, locale: string): string {
  const tool = tools.find((t) => t.id === toolId);
  return tool?.slugs[locale] || tool?.slugs['en'] || toolId;
}

export function getToolsByCategory(category: ToolCategory): ToolDef[] {
  return tools.filter((t) => t.category === category);
}
